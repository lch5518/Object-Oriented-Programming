
public class Identifier {

	public static void main(String[] args) {
		int MyVar = 1, myvar=2;
		int $sum=10, Package=20; //소문자 package는 예약어이기에 사용불가!
		System.out.println("MyVar = " + MyVar + ", myvar = " + myvar);
		System.out.println("$sum = " + $sum + ", Package = " + Package);

	}

}
